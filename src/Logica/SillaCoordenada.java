package Logica;

public class SillaCoordenada {
    private int fila;
    private int columna;

    public SillaCoordenada(int fila, int columna) {
        this.fila = fila;
        this.columna = columna;
    }

    public int getFila() {
        return fila;
    }

    public int getColumna() {
        return columna;
    }
    
    
}
